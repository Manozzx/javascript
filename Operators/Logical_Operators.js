// ! (Logical NOT)

let eligible = false,
    required = true;

console.log(!eligible);//true
console.log(!required);//false


// && (Logical AND)

let eligible1 = false,
    required1 = true;

console.log(eligible1 && required1); // false


// || (Logical OR)

let eligible2 = true,
    required2 = false;

console.log(eligible2 || required2); // true
